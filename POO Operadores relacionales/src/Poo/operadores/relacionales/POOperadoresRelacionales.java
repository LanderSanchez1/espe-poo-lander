public class POOperadoresRelacionales {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int val1= 10;
        int val2= 4;
        System.out.println(val1==val2); //false
        System.out.println(val1!=val2); //true
        System.out.println(val1>val2); //true
        System.out.println(val1<val2); //false
        System.out.println(val1>=val2); //true
        System.out.println(val1<=val2); //false
        
    }
    
}
