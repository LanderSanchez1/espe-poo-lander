    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_jTable1MouseClicked
        int fila = jTable1.getSelectedRow();
        textFieldNombre.setText(jTable1.getValueAt(fila, 0).toString());
        textFieldEdad.setText(jTable1.getValueAt(fila, 1).toString());
        textFieldFechaIngreso.setText(jTable1.getValueAt(fila, 2).toString());
        textFieldSalario.setText(jTable1.getValueAt(fila, 3).toString());
        textFieldHorasVuelo.setText(jTable1.getValueAt(fila, 4).toString());
    }

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton2ActionPerformed
        int fila = jTable1.getSelectedRow();
        if (fila >= 0) {
            listaPilotos.get(fila).setNombre(textFieldNombre.getText());
            listaPilotos.get(fila).setEdad(Integer.parseInt(textFieldEdad.getText()));
            listaPilotos.get(fila).setFechaIngreso(textFieldFechaIngreso.getText());
            listaPilotos.get(fila).setSalarioBase(Double.parseDouble(textFieldSalario.getText()));
            listaPilotos.get(fila).setHorasVuelo(Integer.parseInt(textFieldHorasVuelo.getText()));
            displayPilotos();
            recargarTextField();
        }
    }// GEN-LAST:event_jButton2ActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAgregarActionPerformed
        try {
            String nombre = textFieldNombre.getText();
            int edad = Integer.parseInt(textFieldEdad.getText());
            String fechaIngreso = textFieldFechaIngreso.getText();
            double salario = Double.parseDouble(textFieldSalario.getText());
            int horasVuelo = Integer.parseInt(textFieldHorasVuelo.getText());

            if (nombre.equals("") || edad == 0 || fechaIngreso.equals("") || salario == 0) {
                JOptionPane.showMessageDialog(null, "Debe llenar todos los campos");
            } else {
                Piloto piloto = new Piloto(nombre, edad, fechaIngreso, salario, horasVuelo);
                listaPilotos.add(piloto);
                displayPilotos();
                recargarTextField();
            }
            // Vlidar los numeros int y double en el catch
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Debe ingresar numeros en los campos edad, salario y horas de vuelo");
        }
    }// GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnEliminarActionPerformed
        int fila = JOptionPane.showConfirmDialog(null, "Â¿Desea eliminar el piloto?", "Eliminar",
                JOptionPane.YES_NO_OPTION);
        if (fila == JOptionPane.YES_OPTION) {
            int filaSeleccionada = jTable1.getSelectedRow();
            if (filaSeleccionada >= 0) {
                listaPilotos.remove(filaSeleccionada);
                displayPilotos();
                recargarTextField();
            } else {
                JOptionPane.showMessageDialog(null, "Debe seleccionar una fila");
            }
        }
    }// GEN-LAST:event_btnEliminarActionPerformed

    public void displayPilotos() {
        modelo.setRowCount(0);
        for (int i = 0; i < listaPilotos.size(); i++) {
            modelo.addRow(new Object[] { listaPilotos.get(i).getNombre(), listaPilotos.get(i).getEdad(),
                    listaPilotos.get(i).getFechaIngreso(), listaPilotos.get(i).getSalarioBase(),
                    listaPilotos.get(i).getHorasVuelo() });
        }
    }

    public void recargarTextField() {
        textFieldNombre.setText("");
        textFieldEdad.setText("");
        textFieldFechaIngreso.setText("");
        textFieldSalario.setText("");
        textFieldHorasVuelo.setText("");
    }

